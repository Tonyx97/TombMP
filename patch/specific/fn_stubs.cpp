{
	return static_cast<short>(rcossin_tbl[angle + 1]) >> shift;
}

unsigned long m_atan2(long x1, long y1, long x2, long y2)
{
	// calculate gradient

	auto x = x2 - x1,
		 y = y2 - y1;

	if (x == 0 && y == 0)
		return 0;

	// determine octant

	long octant = 0;

	if (x < 0)
	{
		octant += 4;
		x = -x;
	}

	if (y < 0)
	{
		octant += 2;
		y = -y;
	}

	long n;

	if (y > x)
	{
		++octant;
		n = x;
		x = y;
		y = n;
	}

	while ((short)y != y)		// scale X and Y down into Word
	{							// so that when we shift Y up
		y >>= 1;				// we dont overflow 32 bits!!
		x >>= 1;
	}

	if (x == 0)
		return 0;

	n = atan_table[(y << 11) / x];

	// add on req octant
	return ((n += atan_octant_table[octant]) < 0 ? n & 0xffff : -n & 0xffff);
}